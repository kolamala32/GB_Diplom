# Импорт абстрактного базового класса ABC
from abc import ABC, abstractmethod

# Класс IGUI, являющийся абстрактным базовым классом для интерфейсов
class IGUI(ABC):
    # Абстрактный метод для создания окна
    @abstractmethod
    def create_window(self):
        # Этот метод должен создавать окно для отображения интерфейса
        pass

    # Абстрактный метод для создания кнопок
    @abstractmethod
    def create_buttons(self):
        # Этот метод должен создавать кнопки для выполнения различных действий
        pass

    # Абстрактный метод для создания текстовых полей
    @abstractmethod
    def create_text_fields(self):
        # Этот метод должен создавать текстовые поля для ввода и вывода информации
        pass

    # Абстрактный метод для обработки событий
    @abstractmethod
    def handle_events(self):
        # Этот метод должен обрабатывать события, возникающие при взаимодействии с пользователем
        pass